package com.Freelance.beans;

public class Freelancer {
	private String username;
	private String motdepasse;
	private String nom;
	private String prenom;
	private int prixParheure;
	private int projetsRéalisés;
	private String ville;
	private String Apropos;
	/**
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}
	/**
	 * @return the motdepasse
	 */
	public String getMotdepasse() {
		return motdepasse;
	}
	/**
	 * @param motdepasse the motdepasse to set
	 */
	public void setMotdepasse(String motdepasse) {
		this.motdepasse = motdepasse;
	}
	/**
	 * @return the nom
	 */
	public String getNom() {
		return nom;
	}
	/**
	 * @param nom the nom to set
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}
	/**
	 * @return the prenom
	 */
	public String getPrenom() {
		return prenom;
	}
	/**
	 * @param prenom the prenom to set
	 */
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	/**
	 * @return the prixParheure
	 */
	public int getPrixParheure() {
		return prixParheure;
	}
	/**
	 * @param prixParheure the prixParheure to set
	 */
	public void setPrixParheure(int prixParheure) {
		this.prixParheure = prixParheure;
	}
	/**
	 * @return the projetsRéalisés
	 */
	public int getProjetsRéalisés() {
		return projetsRéalisés;
	}
	/**
	 * @param projetsRéalisés the projetsRéalisés to set
	 */
	public void setProjetsRéalisés(int projetsRéalisés) {
		this.projetsRéalisés = projetsRéalisés;
	}
	/**
	 * @return the ville
	 */
	public String getVille() {
		return ville;
	}
	/**
	 * @param ville the ville to set
	 */
	public void setVille(String ville) {
		this.ville = ville;
	}
	/**
	 * @return the apropos
	 */
	public String getApropos() {
		return Apropos;
	}
	/**
	 * @param apropos the apropos to set
	 */
	public void setApropos(String apropos) {
		Apropos = apropos;
	}
}
